public class RealNumberValues {

    public static void main(String[] args) {
        float minFloat = Float.MIN_VALUE;
        float maxFloat = Float.MAX_VALUE;
        double minDouble = Double.MIN_VALUE;
        double maxDouble = Double.MAX_VALUE;

        System.out.println("float의 최소 양수 값 " + minFloat);
        System.out.println("float의 최대 양수 값 " + maxFloat);
        System.out.println("double의 최소 양수 값" + minDouble);
        System.out.println("double의 최대 양수 값" + maxDouble);
    }
}
